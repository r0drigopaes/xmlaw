package br.pucrio.inf.les.law.component.criticality;

public class Weight {
	private String ref;
	private double value;

	public Weight(String ref, double value) {
		this.ref = ref;
		this.value = value;
	}

	public String getRef() {
		return ref;
	}

	public double getValue() {
		return value;
	}
	
	
	
}
